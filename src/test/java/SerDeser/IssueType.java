package SerDeser;

public class IssueType {
String issuId;

public IssueType(String issuId) {
	
	this.issuId = issuId;
}

/**
 * @return the issuId
 */
public String getIssuId() {
	return issuId;
}

/**
 * @param issuId the issuId to set
 */
public void setIssuId(String issuId) {
	this.issuId = issuId;
}

}
