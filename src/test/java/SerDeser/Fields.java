package SerDeser;

public class Fields {
	Payload fields;

	public Fields(Payload payload) {
		
		this.fields = payload;
	}

	/**
	 * @return the fields
	 */
	public Payload getFields() {
		return fields;
	}

	/**
	 * @param fields the fields to set
	 */
	public void setFields(Payload fields) {
		this.fields = fields;
	}
	

}
